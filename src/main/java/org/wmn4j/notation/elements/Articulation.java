package org.wmn4j.notation.elements;

/**
 * Note specific articulations.
 *
 * @author Otso Björklund
 */
public enum Articulation {
	/**
	 * Specifies an accent marking.
	 */
	ACCENT,

	/**
	 * Specifies a staccato marking.
	 */
	STACCATO,

	/**
	 * Specifies a tenuto marking.
	 */
	TENUTO,

	/**
	 * Specifies a fermata.
	 */
	FERMATA
}
